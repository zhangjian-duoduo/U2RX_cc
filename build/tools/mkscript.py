import os
import sys
import shutil

def mk_ldfile(cfgfile):
    try:
        config = open(cfgfile, 'r')
    except:
        print('open config file: {} failed'.format(cfgfile))
        return

    fastboot = False
    chip_ws  = False
    for aLine in config:
        if aLine.find('CONFIG_FH_FAST_BOOT=y') >= 0:
            fastboot = True
        # if aLine.find('CONFIG_CONFIG_ARCH_FH865x') >= 0:
        #     chip_ws = True
    config.close()

    if fastboot:
        # cp build/fastload.ld as build/baseos.ld
        shutil.copy('build/fastload.ld', 'build/baseos.ld')
        # generate build/media.ld
        medf = open('build/media.ld', 'w')
        medf.write('/* DO NOT EDIT THIS FILE */')
        medf.write('\n')
        medf.write('/* FAST LOAD */')
        medf.write('\n')
        for loops in range(8):
            medf.write('LONG(0)')
            medf.write('\n')
        medf.write('')
        medf.write('\n')
        medf.write('*:*.?sp_app.o(.text .text.*)')
        medf.write('\n')
        medf.write('*libzlib.a:(.text .text.*)')
        medf.write('\n')
        # if chip_ws:
        #     medf.write('*libnna_rtt.a:(.text .text.*)')
        #     medf.write('\n')
        #     medf.write('*libnnpost_rtt.a:(.text .text.*)')
        #     medf.write('\n')
        medf.write('*:*.?sp_app.o(.rodata .rodata.*)')
        medf.write('\n')
        medf.write('*libzlib.a(.rodata .rodata.*)')
        medf.write('\n')
        # if chip_ws:
        #     medf.write('*libnna_rtt.a:(.rodata .rodata.*)')
        #     medf.write('\n')
        #     medf.write('*libnnpost_rtt.a:(.rodata .rodata.*)')
        #     medf.write('\n')
        medf.write('*:*.?sp_app.o(.data .data.*)')
        medf.write('\n')
        medf.write('*libzlib.a(.data .data.*)')
        medf.write('\n')
        # if chip_ws:
        #     medf.write('*libnna_rtt.a:(.data .data.*)')
        #     medf.write('\n')
        #     medf.write('*libnnpost_rtt.a:(.data .data.*)')
        #     medf.write('\n')
        medf.write('')
        medf.write('\n')
        medf.write('__end_of_stage20_code__ = .;')
        medf.write('\n')
        medf.close()
    else:
        # cp build/partload.ld as build/baseos.ld
        shutil.copy('build/partload.ld', 'build/baseos.ld')
        # generate build/media.ld
        medf = open('build/media.ld', 'w')
        medf.write('/* DO NOT EDIT THIS FILE */')
        medf.write('\n')
        medf.write('/* PART LOAD')
        medf.write('\n')
        for loops in range(8):
            medf.write('LONG(0)')
            medf.write('\n')
        medf.write('')
        medf.write('\n')
        medf.write('*:*.?sp_app.o(.text .text.*)')
        medf.write('\n')
        medf.write('*:*.?sp_app.o(.rodata .rodata.*)')
        medf.write('\n')
        medf.write('*:*.?sp_app.o(.data .data.*)')
        medf.write('\n')
        medf.write('*/')
        medf.write('\n')
        medf.write('__end_of_stage20_code__ = .;')
        medf.write('\n')
        medf.close()

if __name__ == '__main__':
    mk_ldfile(".config")
